import math
print(math.pi)  # 3.141592653589793
print(math.e)  # 2.718281828459045
print(math.pow(4, 3))  # 64.0
print(math.sqrt(25))  # 5.0
print(math.floor(5.6666))  # 5
print(math.ceil(5.7777)) # 6
print(math.radians(90)) # 1.5707963267948966
print(math.degrees(1.57)) # 89.95437383553924
print(math.log10(60)) # 1.7781512503836436
print(math.sin(90)) # 0.8939966636005579
print(math.factorial(23)) # 25852016738884976640000
print(math.gcd(34,88)) # наибольший общий делитель 2
